# Наследуется всё, что не является приватным


# 1. Создаем родительский класс со всеми типами членов:
class Device:
    # Атрибут класса (публичный)
    company = "FutureTech"

    def __init__(self, model_name, power):
        # Атрибуты экземпляра (публичные и защищенный)
        self.model_name = model_name
        self._power_level = power  # Защищенный
        self.__secret_code = "XYZ123"  # Приватный

    # Публичный метод
    def turn_on(self):
        print(f"Устройство {self.model_name} включено.")

    # Защищенный метод
    def _run_diagnostics(self):
        print(f"Запуск диагностики... Уровень мощности: {self._power_level}")

    # Приватный метод
    def __get_secret_code(self):
        return self.__secret_code


# 2. Создаем дочерний класс и добавляем в него метод, который попытается получить доступ ко всему:
class CoffeeMachine(Device):
    """Кофемашина наследует от Устройства."""

    def make_coffee(self):
        print("\n--- Готовлю кофе ---")
        # 1. Доступ к публичным членам родителя: УСПЕХ
        print(f"Производитель: {self.company}")
        print(f"Модель: {self.model_name}")
        self.turn_on()  # Вызов публичного метода родителя

        # 2. Доступ к защищенным членам родителя: УСПЕХ
        # (Так можно делать, потому что мы "семья" - дочерний класс)
        print(f"Текущая мощность: {self._power_level}")
        self._run_diagnostics()  # Вызов защищенного метода родителя

        # 3. Доступ к приватным членам родителя: ОШИБКА!
        try:
            print(self.__secret_code)
        except AttributeError as e:
            print(f"Не удалось получить доступ к __secret_code: {e}")


# 3. Тестируем наш дочерний класс:
# Создаем экземпляр ДОЧЕРНЕГО класса
my_coffee_machine = CoffeeMachine("LatteMaster 5000", 1500)

# Вызываем собственный метод дочернего класса
my_coffee_machine.make_coffee()

# Публичные company, model_name и turn_on(): Доступны без каких-либо проблем,
# как будто они определены прямо в классе CoffeeMachine

# Защищенные _power_level и _run_diagnostics(): Также доступны. Соглашение об одном подчеркивании (_) не запрещает доступ из дочерних классов
# Это считается нормальной практикой, так как дочерний класс является "близким родственником" и ему может понадобиться доступ к "служебной" функциональности родителя

# Приватные __secret_code: Недоступны
# Механизм искажения имен переименовал __secret_code в _Device__secret_code, и класс CoffeeMachine ничего об этом новом имени не знает
