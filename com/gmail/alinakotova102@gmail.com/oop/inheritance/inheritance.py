# 1. Определяем родительский (базовый) класс:
class Animal:
    """Это наш базовый класс для всех животных."""

    def __init__(self, name):
        self.name = name
        print(f"Родилось животное по имени {self.name}")

    def eat(self):
        """Общий метод для всех животных."""
        print(f"{self.name} ест.")

    def sleep(self):
        """Еще один общий метод."""
        print(f"{self.name} спит.")


# 2. Определяем дочерний класс, используя синтаксис наследования:
class Dog(Animal):
    """
    Это дочерний класс. Он наследует всё от класса Animal.
    """

    pass


# 3. Проверяем результат:
# Создаем объект именно дочернего класса Dog
my_dog = Dog("Рекс")

# А теперь вызываем методы, которые были определены в РОДИТЕЛЬСКОМ классе!
my_dog.eat()
my_dog.sleep()

# Мы даже можем получить доступ к атрибуту, который был создан в __init__ родителя!
print(f"Имя моей собаки: {my_dog.name}")

# !! Когда мы написали my_dog = Dog("Рекс"), Python увидел, что у Dog нет своего собственного __init__
# Поэтому он "пошел наверх" по цепочке наследования и вызвал __init__ из родительского класса Animal

# !! Когда мы вызвали my_dog.eat(), Python сначала поискал метод eat в классе Dog. Не нашел
# Тогда он снова "пошел наверх" к Animal, нашел там этот метод и успешно его выполнил

