# Вызов метода класса

# Как заставить собаку лаять?
# Метод принадлежит классу, но вызывается он у конкретного объекта (экземпляра). Мы не можем попросить "чертеж" лаять, мы можем попросить лаять настоящую собаку

# 1. Сначала создаем объект, как мы научились в прошлом шаге
my_dog = Dog()

# 2. Теперь вызываем его метод.
# Мы обращаемся к объекту my_dog и через точку даем ему команду выполнить действие bark
my_dog.bark()  # Выведет в консоль: Гав-гав!

# Можно создать другой объект и он тоже будет уметь это делать
another_dog = Dog()
another_dog.bark()  # Выведет в консоль: Гав-гав!
