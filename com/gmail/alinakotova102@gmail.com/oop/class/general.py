# Методы — это поведение объекта
# Метод (method) — это функция, которая определена внутри класса и принадлежит объекту. Методы — это то, что объект умеет делать

# методы — это главный и самый правильный способ изменять атрибуты своего объекта.

# Когда вы вызываете метод у конкретного объекта, например my_dog.eat(),
# Python делает одну очень важную вещь: он неявно передает ссылку на сам объект my_dog в этот метод в качестве первого аргумента, self

# !! self ("this" in Java) — это специальное имя (по соглашению) для параметра, который указывает на сам экземпляр объекта
# Это мост, который позволяет методу получить доступ к атрибутам и другим методам того же самого объекта


class Cat:
    # Конструктор, который создает атрибуты
    def __init__(self, name):
        print(f"Кота по имени {name} создали!")
        self.name = name
        self.satiety = 5  # Начальное состояние сытости

    # Метод, который ЧИТАЕТ атрибуты
    def show_status(self):
        print(f"Имя: {self.name}, Сытость: {self.satiety}")

    # Метод, который ИЗМЕНЯЕТ атрибут
    def eat(self):
        print(f"{self.name} ест...")
        # Мы используем self, чтобы получить доступ к АТРИБУТУ этого же объекта
        self.satiety = self.satiety + 3
        print(f"Сытость увеличилась!")


# --- Демонстрация ---

# 1. Создаем объект. Вызывается __init__, устанавливается начальное состояние.
barsik = Cat("Барсик")

# 2. Проверяем его исходное состояние.
barsik.show_status()  # Выведет: Имя: Барсик, Сытость: 5

# 3. Вызываем метод, который изменит состояние объекта.
# В этот момент Python передает ссылку на 'barsik' в метод 'eat' как 'self'.
barsik.eat()

# 4. Проверяем состояние еще раз. Оно изменилось!
barsik.show_status()  # Выведет: Имя: Барсик, Сытость: 8


# Этот механизм — основа инкапсуляции. Мы не меняем сытость кота напрямую снаружи (barsik.satiety = 1000),
# что может привести к ошибкам (например, нелогично большому значению)
# Вместо этого мы просим объект "поесть" с помощью метода eat()
# Это позволяет в будущем добавить в метод eat() дополнительную логику:
# например, проверку, чтобы сытость не превышала 10, или уменьшать количество еды в миске.

