# Что такое метод?

# Метод — это, по своей сути, обычная функция, но с одним ключевым отличием:
# она определена внутри тела класса и неразрывно связана с ним. Если атрибуты — это что объект есть (его характеристики),
# то методы — это что объект умеет делать (его действия)

# Синтаксис определения метода точно такой же, как и у обычной функции — мы используем ключевое слово def


class Dog:
    # Это наш первый метод!
    # Он определен с отступом, поэтому принадлежит классу Dog
    def bark(self):
        print("Гав-гав!")


# Давайте разберем эту конструкцию:

# - def bark(self): — Мы определяем функцию с именем bark внутри класса Dog. Теперь это не просто функция, а метод класса Dog

# - self — Обратите внимание на этот загадочный параметр. Это самое важное отличие метода от обычной функции
# Каждый метод, который работает с экземпляром объекта, должен принимать self в качестве своего первого параметра
# Мы подробно разберем его роль в следующем шаге, а пока просто запомните, что он должен быть всегда

# - print("Гав-гав!") — Тело метода, которое содержит код, описывающий его поведение. В данном случае, наша собака просто выводит текст в консоль


