class WebsiteUser:
    # Атрибут класса для подсчета пользователей
    total_users = 0

    def __init__(self, nickname):
        self.nickname = nickname
        # Каждый раз, когда создается новый пользователь (вызывается __init__),
        # мы увеличиваем ОБЩИЙ счетчик.
        WebsiteUser.total_users = WebsiteUser.total_users + 1
        print(
            f"Пользователь {self.nickname} зарегистрирован. Всего пользователей: {WebsiteUser.total_users}"
        )


# --- Смотрим в действии ---

print(f"Изначально пользователей: {WebsiteUser.total_users}")

user1 = WebsiteUser("Alice")
user2 = WebsiteUser("Bob")
user3 = WebsiteUser("Charlie")

print(f"\nИтоговый подсчет: {WebsiteUser.total_users}")
print()

