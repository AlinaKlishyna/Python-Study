# Атрибуты класса. Общие данные для всех объектов

# В прошлом шаге мы говорили о персональных данных, уникальных для каждого объекта
# Но что, если какая-то информация должна быть одинаковой и общей для всех объектов, созданных по одному классу? Например, все студенты учатся в одном и том же университете
# Было бы неэффективно и неправильно хранить название университета в каждом отдельном объекте-студенте


# !! Атрибут класса — это переменная, которая принадлежит самому классу, а не какому-то конкретному экземпляру. Она является общей, и все объекты этого класса "видят" одно и то же значение
class Student:
    # --- Это атрибут класса ---
    # Он принадлежит классу Student в целом.
    university_name = "Технологический Университет Python"

    def __init__(self, name, faculty):
        # А это атрибуты экземпляра
        self.name = name
        self.faculty = faculty

    def show_info(self):
        # Обратите внимание: мы можем получить доступ к атрибуту класса через self
        print(f"Студент: {self.name}, Университет: {self.university_name}")


