class Config:
    theme = "light"

    def __init__(self, app_name, theme):
        self.app_name = app_name
        self.theme = theme


config_1 = Config("Фоторедактор", "red")
config_2 = Config("Календарь", Config.theme)

print(f"Тема для {config_1.app_name}: {config_1.get_theme()}")  # Вывод: red
print(f"Тема для {config_2.app_name}: {config_2.get_theme()}")  # Вывод: light
print()
