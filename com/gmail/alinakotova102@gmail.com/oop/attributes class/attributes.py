# что будет, если имена атрибутов совпадут?
# атрибуты класса — общие, а атрибуты экземпляра — персональные
# Но возникает продвинутый и очень важный вопрос: что произойдет, если мы создадим атрибут экземпляра с таким же именем, как у атрибута класса?
# !! Правило: Когда вы обращаетесь к атрибуту через экземпляр (например, my_obj.value), Python сначала ищет атрибут value в самом экземпляре
# !! Если не находит, он продолжает поиск на уровне класса


class Config:
    # Атрибут КЛАССА: общая настройка для всех
    theme = "light"

    def __init__(self, app_name):
        self.app_name = app_name

    def get_theme(self):
        # self.theme сначала будет искаться в экземпляре, потом в классе
        return self.theme


# ================================================================

# --- Сценарий 1: Все используют общую тему ---
config_1 = Config("Фоторедактор")
config_2 = Config("Календарь")

print(f"Тема для {config_1.app_name}: {config_1.get_theme()}")  # Вывод: light
print(f"Тема для {config_2.app_name}: {config_2.get_theme()}")  # Вывод: light

print("\n--- Администратор меняет общую тему ---")

# Изменяем атрибут КЛАССА. Это изменение увидят ВСЕ экземпляры.
Config.theme = "dark"

print(f"Новая тема для {config_1.app_name}: {config_1.get_theme()}")  # Вывод: dark
print(f"Новая тема для {config_2.app_name}: {config_2.get_theme()}")  # Вывод: dark

print()

# ================================================================
# --- Сценарий 2: Один из пользователей ставит себе персональную тему ---

print("\n--- Пользователь 1 ставит себе особую тему ---")

# ВАЖНО: Эта строка создает НОВЫЙ атрибут ЭКЗЕМПЛЯРА 'theme'
# ТОЛЬКО для объекта config_1. Он "затеняет" атрибут класса.
config_1.theme = "blue_sky"

print(
    f"Персональная тема для {config_1.app_name}: {config_1.get_theme()}"
)  # Вывод: blue_sky
# А у config_2 своего атрибута 'theme' нет, поэтому он продолжает "видеть" атрибут класса.
print(
    f"Тема для {config_2.app_name} осталась прежней: {config_2.get_theme()}"
)  # Вывод: dark
print()


