# !! задавайте себе один простой вопрос: "Эти данные являются персональными для каждого отдельного объекта или они общие для всех?"
# - Если данные персональные (у каждого объекта свое значение) — это атрибут экземпляра.
# - Если данные общие (одно значение на всех) — это атрибут класса.

# ================================================================


class Car:
    # --- Атрибуты класса ---
    # Эти данные общие для ВСЕХ машин, которые мы создадим.
    number_of_wheels = 4
    cars_produced = 0

    def __init__(self, model, color, year):
        # --- Атрибуты экземпляра ---
        # Эти данные уникальны для КАЖДОЙ машины.
        self.model = model
        self.color = color
        self.year = year
        self.is_engine_on = False

        # Увеличиваем общий счетчик при создании новой машины
        Car.cars_produced += 1

    def start_engine(self):
        self.is_engine_on = True
        print(f"Двигатель машины {self.model} заведен.")


# --- Работа с объектами ---

# Создаем два разных объекта
car_1 = Car("Tesla Model S", "Красный", 2022)
car_2 = Car("Ford Mustang", "Черный", 1968)

# Проверяем их персональные данные (атрибуты экземпляра)
print(f"Моя первая машина: {car_1.model}, цвет {car_1.color}")
print(f"Моя вторая машина: {car_2.model}, цвет {car_2.color}")

print("---")

# Проверяем общие данные (атрибуты класса)
# Доступ можно получить и через класс, и через объект
print(f"У машины {car_1.model} {car_1.number_of_wheels} колеса.")
print(f"Всего произведено машин: {Car.cars_produced}")
print()

