# id — это имя встроенной в Python функции


# В Python есть функция id(), которая возвращает уникальный "адрес" объекта в памяти
my_list = [1, 2, 3]
print(id(my_list))  # Выведет что-то вроде 2191564506432


# Если мы назовем переменную или параметр id, мы "затеним" (shadow) эту встроенную функцию. Она перестанет быть доступной в той области видимости, где мы создали свою переменную
# Пример проблемы:
def create_user(name, id):  # Плохое имя для параметра!
    print(f"Создаем пользователя {name} с id = {id}")
    # Теперь мы не можем использовать встроенную функцию id() внутри этой функции
    # Этот вызов вызовет ошибку TypeError: 'int' object is not callable
    print(id(name))


create_user("Alice", 101)
