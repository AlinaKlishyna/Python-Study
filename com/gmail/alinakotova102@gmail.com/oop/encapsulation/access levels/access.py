# Давайте перепишем наш класс SafeBankAccount, следуя этому соглашению. Мы переименуем атрибут balance в _balance, чтобы показать, что он защищенный.
class BankAccount:
    def __init__(self, owner, balance):
        self.owner = owner  # Публичный атрибут - имя владельца можно читать
        self._balance = balance  # ЗАЩИЩЕННЫЙ атрибут - баланс напрямую трогать не надо!

    # --- Публичный интерфейс ---

    def deposit(self, amount):
        if amount > 0:
            self._balance += amount
            print(f"Счет пополнен. Новый баланс: {self._balance}")
        else:
            print("Ошибка: сумма должна быть положительной.")

    def withdraw(self, amount):
        if 0 < amount <= self._balance:
            self._balance -= amount
            print(f"Снятие успешно. Новый баланс: {self._balance}")
        else:
            print("Ошибка: некорректная сумма или недостаточно средств.")

    def get_balance(self):
        """Единственный "официальный" способ узнать баланс."""
        return self._balance


account = BankAccount("Анна", 1000)

# --- ПРАВИЛЬНОЕ использование через публичный интерфейс ---
account.deposit(500)
print(f"Текущий баланс: {account.get_balance()}")

print("\n--- Нарушаем соглашение (что делать НЕ НАДО) ---")

# Технически, мы все еще можем это сделать...
print(f"Читаем защищенный атрибут напрямую: {account._balance}")

# ...и даже можем его сломать!
account._balance = -500  # Напрямую присваиваем некорректное значение
print(f"Новый баланс после прямого вмешательства: {account._balance}")

# Объект теперь в некорректном, "сломанном" состоянии!
# Наша логика в методе withdraw() уже может работать неправильно
account.withdraw(100)

