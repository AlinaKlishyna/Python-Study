# private ================================================================
# Приватный член (private member) — это атрибут или метод, имя которого начинается с двух (но не заканчивается ими) символов подчеркивания. Например, __secret_key или __calculate_internals()
# Использование __ запускает специальный механизм Python, который называется Искажение имен (Name Mangling)
class SecretBox:
    def __init__(self, owner, secret_code):
        self.owner = owner
        self.__secret_code = secret_code  # Это приватный атрибут

    def __internal_method(self):
        """Это приватный метод."""
        print("Выполняется внутренний секретный метод...")

    def reveal_secret(self):
        """Это публичный метод для доступа к приватным данным."""
        print(f"Секретный код для {self.owner}: {self.__secret_code}")
        self.__internal_method()


# --- Пытаемся получить доступ извне ---

box = SecretBox("Боб", "12345-ABC")

# Пытаемся прочитать приватный атрибут напрямую...
try:
    print(box.__secret_code)
except AttributeError as e:
    print(
        f"Ошибка! {e}"
    )  # Выведет: Ошибка! 'SecretBox' object has no attribute '__secret_code'

# Пытаемся вызвать приватный метод напрямую...
try:
    box.__internal_method()
except AttributeError as e:
    print(
        f"Ошибка! {e}"
    )  # Выведет: Ошибка! 'SecretBox' object has no attribute '__internal_method'


# Мы все еще можем получить доступ, если знаем искаженное имя
print("\n--- Обходим защиту (не делайте так!) ---")
print(f"Реальное имя атрибута: _SecretBox__secret_code")
print(f"Доступ к коду через искаженное имя: {box._SecretBox__secret_code}")

