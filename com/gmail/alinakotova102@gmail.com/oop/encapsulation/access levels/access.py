# public ================================================================
# Публичный член (атрибут или метод) — это любой член класса, к которому можно свободно обращаться из любой части программы
# Любой атрибут или метод, имя которого не начинается со знака подчеркивания _, по умолчанию считается публичным
class User:
    def __init__(self, username, email):
        # Эти атрибуты - публичные
        self.username = username
        self.email = email
        self.is_active = True

    # Этот метод - публичный
    def greet(self):
        """Приветствует пользователя. Часть публичного интерфейса."""
        print(f"Привет, {self.username}!")

    # Этот метод - тоже публичный
    def deactivate(self):
        """Деактивирует пользователя. Часть публичного интерфейса."""
        self.is_active = False
        print(f"Пользователь {self.username} был деактивирован.")


# Что значит "доступ без ограничений"?

# Это значит, что после создания объекта вы можете как читать, так и изменять его публичные атрибуты напрямую, из любого места в вашем коде
# Создаем объект
user = User("Alice", "alice@example.com")

# --- Чтение публичных атрибутов ---
print(f"Имя пользователя: {user.username}")
print(f"Активен ли: {user.is_active}")

# --- Вызов публичных методов ---
user.greet()

print("---")

# --- Прямое изменение публичных атрибутов ---
# Технически это возможно, но часто это плохая практика!
user.username = "SuperAlice"
user.is_active = 12345  # Можно даже записать некорректный тип данных

print(f"Новое имя пользователя: {user.username}")
print(f"Новый статус активности: {user.is_active}")  # 12345

user.deactivate()
print(f"Статус после деактивации: {user.is_active}")  # False
print("\n")


