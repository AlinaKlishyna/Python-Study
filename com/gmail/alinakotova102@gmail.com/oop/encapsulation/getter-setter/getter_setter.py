# Методы-сеттеры (setters). Контроль и валидация при записи =========================================
class User:
    def __init__(self, name, age):
        self.name = name
        # При создании объекта мы тоже должны проверить возраст!
        # Поэтому здесь мы вызываем наш же сеттер.
        self._age = 0  # Задаем безопасное значение по умолчанию
        self.set_age(age)  # Используем сеттер для начальной установки

    def get_age(self):
        """Геттер для атрибута _age."""
        return self._age

    # --- Наш первый сеттер ---
    def set_age(self, new_age):
        """
        Это сеттер для атрибута _age.
        Он проверяет данные перед установкой.
        """
        print(f"Попытка установить возраст: {new_age}")

        # 1. Проверка на ТИП данных
        if not isinstance(new_age, int):
            print("Ошибка: возраст должен быть целым числом.")
            return  # Прерываем выполнение метода

        # 2. Проверка на ДИАПАЗОН значения
        if 0 <= new_age <= 120:
            # Если все проверки пройдены, сохраняем значение
            self._age = new_age
            print("Возраст успешно изменен.")
        else:
            # Если данные некорректны, отклоняем изменение
            print("Ошибка: возраст должен быть в диапазоне от 0 до 120.")

    def show_info(self):
        print(f"Пользователь: {self.name}, Возраст: {self._age}")


# !! Обратите внимание на __init__! Мы изменили его так, чтобы он тоже использовал сеттер
# !! Это гарантирует, что даже при создании объекта данные пройдут ту же самую проверку

user = User("Иван", 30)  # Вывод: Попытка... Возраст успешно изменен.
user.show_info()  # Пользователь: Иван, Возраст: 30
print("---")

