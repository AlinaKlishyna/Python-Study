# Методы-геттеры (getters) =========================================
class User:
    def __init__(self, name, age):
        self.name = name
        self._age = age  # Атрибут для хранения возраста

    def show_info(self):
        # Внутри класса мы можем безопасно использовать _age
        print(f"Пользователь: {self.name}, Возраст: {self._age}")

    # --- Наш первый геттер ---
    def get_age(self):
        """
        Это геттер для атрибута _age.
        Он является частью публичного интерфейса.
        """
        return self._age


