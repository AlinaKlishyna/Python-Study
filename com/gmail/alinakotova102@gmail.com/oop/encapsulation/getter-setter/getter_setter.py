# Реализация инкапсуляции с помощью геттеров и сеттеров =========================================
class BankAccount:
    """
    Класс, который полностью инкапсулирует данные о балансе,
    предоставляя доступ только через геттер и сеттер.
    """

    def __init__(self, owner, initial_balance):
        self.owner = owner

        # 1. ЗАЩИЩЕННЫЙ АТРИБУТ для хранения данных
        self._balance = 0.0

        # Используем сеттер даже при создании, чтобы проверить начальные данные
        self.set_balance(initial_balance)

    # 2. ПУБЛИЧНЫЙ ГЕТТЕР для чтения
    def get_balance(self):
        """Возвращает текущий баланс."""
        return self._balance

    # 3. ПУБЛИЧНЫЙ СЕТТЕР для записи
    def set_balance(self, new_balance):
        """Устанавливает новый баланс после проверок."""

        # Валидация: проверяем, что нам передали число (int или float)
        if not isinstance(new_balance, (int, float)):
            print("Ошибка: баланс должен быть числом.")
            return

        # Валидация: проверяем, что число не отрицательное
        if new_balance < 0:
            print("Ошибка: баланс не может быть отрицательным.")
            return

        # Если все проверки пройдены, обновляем защищенный атрибут
        self._balance = float(new_balance)
        print(f"Баланс успешно установлен: {self._balance}")


account = BankAccount("Дмитрий", 1000)  # Баланс успешно установлен: 1000.0

# --- Чтение данных ---
# Мы используем геттер - безопасный и официальный способ
current_balance = account.get_balance()
print(f"Текущий баланс, полученный через геттер: {current_balance}")

# --- Попытка изменить данные ---

print("\nПопытка установить корректное значение:")
# Мы используем сеттер. Данные проходят проверку и сохраняются.
account.set_balance(1500.50)
print(f"Новый баланс: {account.get_balance()}")

print("\nПопытка установить НЕкорректное значение:")
# Мы используем сеттер. Данные НЕ проходят проверку.
account.set_balance(-500)
# Состояние объекта не изменилось!
print(f"Баланс после неудачной попытки: {account.get_balance()}")
