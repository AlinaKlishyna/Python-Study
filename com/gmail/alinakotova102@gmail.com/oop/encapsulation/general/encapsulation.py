# Инкапсуляция — это механизм, который объединяет данные и код, манипулирующий этими данными, в единый объект,
# а также скрывает внутреннюю реализацию этого объекта от внешнего мира

# Публичный интерфейс — это набор публичных методов и атрибутов, которые разработчик класса преднамеренно делает доступными для использования извне
# Это официальный и безопасный способ взаимодействия с объектом


# Вариант 1: Плохой дизайн (без публичного интерфейса) ========================================================
class UnsafeBankAccount:
    def __init__(self, owner, balance):
        self.owner = owner
        self.balance = balance  # Просто публичный атрибут


# Теперь другой программист (или вы сами через месяц) начинает использовать этот класс
account = UnsafeBankAccount("Иван", 1000)

# Программист решает снять деньги
print(f"Баланс до: {account.balance}")  # 1000
account.balance = account.balance - 1500  # Снимаем больше, чем есть!
print(f"Баланс после: {account.balance}")  # -500. Упс! Банк в убытке.

# Другой программист делает опечатку
account.balance = account.balance + "200"  # Пытается сложить число и строку
# ПРОГРАММА УПАДЕТ с ошибкой TypeError!

# В этом варианте объект BankAccount — это просто пассивный контейнер для данных
# Он не имеет никакого контроля над своим состоянием
# Вся ответственность за правильность данных лежит на внешнем коде, что неизбежно приведет к ошибкам


# Вариант 2: Хороший дизайн (с публичным интерфейсом) ========================================================
# Теперь создадим "безопасные" методы deposit (внести) и withdraw (снять), которые и станут нашим публичным интерфейсом
class SafeBankAccount:
    def __init__(self, owner, balance):
        self.owner = owner
        # Мы сделаем этот атрибут "защищенным" позже, а пока сосредоточимся на методах
        self.balance = balance

    # --- Публичный интерфейс ---

    def deposit(self, amount):
        """Вносит деньги на счет. Сумма должна быть положительной."""
        if amount > 0:
            self.balance += amount
            print(f"Счет пополнен на {amount}. Новый баланс: {self.balance}")
        else:
            print("Ошибка: сумма для пополнения должна быть положительной.")

    def withdraw(self, amount):
        """Снимает деньги со счета. Сумма должна быть положительной и не превышать баланс."""
        if amount <= 0:
            print("Ошибка: сумма для снятия должна быть положительной.")
        elif self.balance >= amount:
            self.balance -= amount
            print(f"Со счета снято {amount}. Новый баланс: {self.balance}")
        else:
            print(f"Ошибка: недостаточно средств. Баланс: {self.balance}")

    def get_balance(self):
        """Возвращает текущий баланс."""
        return self.balance


safe_account = SafeBankAccount("Анна", 1000)

# Пользуемся "кнопками" на пульте управления
safe_account.deposit(500)  # Все хорошо
safe_account.withdraw(200)  # Все хорошо
safe_account.withdraw(
    1500
)  # Объект защитил себя! Выведет: Ошибка: недостаточно средств.
safe_account.deposit(
    -100
)  # Объект защитил себя! Выведет: Ошибка: сумма должна быть положительной.

print(
    f"\nИтоговый баланс: {safe_account.get_balance()}"
)  # 1300. Состояние объекта корректно.


# !! - Публичный интерфейс — это ваш "пульт управления" объектом.
# !! - Он состоит из "безопасных" методов, которые содержат проверки и бизнес-логику.
# !! - Использование интерфейса вместо прямого доступа к атрибутам защищает объект от некорректного состояния и делает программу в целом намного надежнее.


